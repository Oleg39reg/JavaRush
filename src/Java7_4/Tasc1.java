package Java7_4;

public class Tasc1 {
    public static void main(String[] args) {
        everOrOdd(7);
    }

    public static void everOrOdd(int number) {
        if (number % 2 == 0) {
            System.out.println("Чётное");
        } else
            System.out.println("Не чётное");
    }
}


