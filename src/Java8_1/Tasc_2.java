package Java8_1;

public class Tasc_2 {
    static byte q = Byte.MAX_VALUE;
    static short w = Short.MAX_VALUE;
    static int e = Integer.MAX_VALUE;
    static long r = Long.MAX_VALUE;

    public static void main(String[] args) {
        System.out.println("Максимальная величина типа byte: "+q);
        System.out.println("Максимальная величина типа short: "+w);
        System.out.println("Максимальная величина типа int: "+e);
        System.out.println("Максимальная величина типа long: "+r);
    }
}
