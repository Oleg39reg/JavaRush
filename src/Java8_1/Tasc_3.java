package Java8_1;

public class Tasc_3 {
    static byte q = Byte.MIN_VALUE;
    static short w = Short.MIN_VALUE;
    static int e = Integer.MIN_VALUE;
    static long r = Long.MIN_VALUE;

    public static void main(String[] args) {
        System.out.println("Минимальная величина типа byte: "+q);
        System.out.println("Минимальная величина типа short: "+w);
        System.out.println("Минимальная величина типа int: "+e);
        System.out.println("Минимальная величина типа long: "+r);
    }
}
